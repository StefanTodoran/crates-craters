// EXPORTING
export let theme = "purple";
export let colors = purple_colors;
export let graphics = purple_graphics;

// CHANGING THEME
let current = 2;
const rotation = ["blue", "orange", "purple"];
const colors_rotation = [blue_colors, orange_colors, purple_colors];
const graphics_rotation = [blue_graphics, orange_graphics, purple_graphics];

export function nextTheme() {
  current = (current + 1 < rotation.length) ? current + 1 : 0;
  theme = rotation[current];
  colors = colors_rotation[current];
  graphics = graphics_rotation[current];
  return theme;
}